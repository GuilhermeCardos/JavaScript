/*
    SPA (single page Application)
        Aplicativo de página única: A concentração do aplicativo web é feita em uma única página, com isso, quando o site e carregado no lado do cliente, e carregado por completo e de acordo o usuario vai interagindo só é carregado aquele componente na mesma página, isto é sem sair da página atual.
        Ex: Facebook, trello, notion, ...
    
    PWA (Progressive Web App)
        Aplicativo web progressivo: É um site desenvolvido para parecer e se comportar como um aplicativo nativo independente da plataforma em que é utilizado. Browser/Mobile
    
    WEB COMPONENTS
        *Elementos Personalizados:
            Criação de components reutilizaveis no desenvolvimento web de forma nativa:
            Conjunto de APIs js que permitem difinir elementos personalizados e seu comportamentos     
        *Shadow DOM:
            Conjunto de APIs para anexar uma árvore DOM
        *Modelos HTML:
            Os elementos <tamplate> e <slot> permite que se escreva modelos de marcação (HTML) que não serão exibidos na página redenrizada. 
            Eles podem ser reutilizada várias vezes. 
*/